package com.weakie.driving.model.system.arc;

public class RegularIncrease {

	private int w;
	private int x;
	private int y;
	private int z;
	private int a;
	private int b;
	private int c;
	private int d;
	
	public RegularIncrease() {
		super();
	}
	public RegularIncrease(int w, int x, int y, int z, int a, int b, int c, int d) {
		super();
		this.w = w;
		this.x = x;
		this.y = y;
		this.z = z;
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}
	public int getW() {
		return w;
	}
	public void setW(int w) {
		this.w = w;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getZ() {
		return z;
	}
	public void setZ(int z) {
		this.z = z;
	}
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int getC() {
		return c;
	}
	public void setC(int c) {
		this.c = c;
	}
	public int getD() {
		return d;
	}
	public void setD(int d) {
		this.d = d;
	}
	@Override
	public String toString() {
		return "RegularIncrease [w=" + w + ", x=" + x + ", y=" + y + ", z=" + z + ", a=" + a + ", b=" + b + ", c=" + c
				+ ", d=" + d + "]";
	}
	
	
}
